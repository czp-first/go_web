// 8 Web服务
// web服务可以让你在HTTP协议的基础上通过XML或者JSON来交换信息。如果你想知道上海的天气预报、中国石油的股价或者淘宝商家的一个商品信息，你可以编写一段简短的代码，通过抓取这些信息然后通过标准的接口开放出来，就如同调用一个本地函数并返回一个值
// web服务背后的关键在于平台的无关性，可以运行服务在linux系统，可以与其他window的asp.net程序交互，同样的，也可以通过同一个接口和运行在FreeBSD上面的JSP无障碍地通信
// 目前主流的有如下几种web服务：REST、SOAP
// REST请求是很直观的，因为REST是基于HTTP协议的一个补充，它的每一次请求都是一个HTTP请求，然后根据不同的method来处理不同的逻辑，有很多web开发者都熟悉HTTP协议，所以学习REST是一件比较容易的事情
// SOAP是W3C在跨网络信息传递和远程计算机函数调用方面的一个标准。但是SOAP非常复杂，其完整的规范篇幅很长，而且内容仍然在增加。go是以简单著称，所以作者不会介绍SOAP这样复杂的东西。而go提供了一种天生性能很不错，开发起来很方便的RPC机制，



